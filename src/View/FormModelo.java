        if (txtIdP.getText().trim().equals("")) {
//            ClienteJpaController cc = new ClienteJpaController(emf);
//            if(cc.existeCodigo(txtNuit.getText())!=0){
//                lbValidacao.setVisible(true);
//                lbValidacao.setText("Este codigo ja existe. Introduza outro");
//                txtNuit.requestFocusInWindow();
//                return;
//            }
            Modelo p = new Modelo();
            //  pro.setNuit(txtNuit.getText());
            p.setDescricao(txmodelo.getText());
            p.setSerie(txserie.getText());
            p.setTipo(txtipo.getText());
            p.setMarca((Marca) cbMarca.getSelectedItem());
            new ModeloJpaController(emf).create(p);
            limpar();
            JOptionPane.showMessageDialog(this, "Adicionado com Sucesso");
        } else {
            int respo = JOptionPane.showOptionDialog(null, "Pretende modificar os dados deste Modelo?", "ATENÇÃO!", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE,
                    null, new String[]{"SIM", "NÃO"}, null);
            if (respo == 1 || respo == -1) {
                return;
            }
            ModeloJpaController cc = new ModeloJpaController(emf);
            Modelo p = cc.findModelo(Long.parseLong(txtIdP.getText()));
            p.setDescricao(txmodelo.getText());
            p.setSerie(txserie.getText());
            p.setTipo(txtipo.getText());
            p.setMarca((Marca) cbMarca.getSelectedItem());
            try {
                cc.edit(p);
            } catch (Exception ex) {
                Logger.getLogger(FormModelo.class.getName()).log(Level.SEVERE, null, ex);
            }
            JOptionPane.showMessageDialog(this, "Editado com Sucesso");
            this.setVisible(false);
        }

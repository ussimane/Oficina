    private void btdelfamActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btdelfamActionPerformed
        Marca m = (Marca) cbMarca.getSelectedItem();
        if (txtIdP.getText().trim().equals("")) {
            if (new ModeloJpaController(emf).existeMarca(m) != 0) {
                JOptionPane.showMessageDialog(this, "Esta Marca esta em uso! Nao pode ser apagada", "Atencao", JOptionPane.WARNING_MESSAGE);
                return;
            }
        } else {
            try {
                new MarcaJpaController(emf).destroy(m.getIdmarca());
            } catch (NonexistentEntityException ex) {
                Logger.getLogger(FormMarca.class.getName()).log(Level.SEVERE, null, ex);
            }
            JOptionPane.showMessageDialog(this, "Marca apagada com Sucesso");
        }
        combobox2(cbMarca);
    }//GEN-LAST:event_btdelfamActionPerformed

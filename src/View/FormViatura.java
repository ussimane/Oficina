    private void btdelimpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btdelimpActionPerformed
        Modelo i = (Modelo) cbmodelo.getSelectedItem();
        int ind = cbmodelo.getSelectedIndex();
        if (txtIdP.getText().trim().equals("")) {
            if (new ProdutoJpaController(emf).existeModelo(i) != 0) {
                JOptionPane.showMessageDialog(this, "Este Modelo esta em uso! Nao pode ser apagado", "Atencao", JOptionPane.WARNING_MESSAGE);
                return;
            }
        } else {
            try {
                new ModeloJpaController(emf).destroy(i.getIdmodelo());
            } catch (NonexistentEntityException ex) {
                Logger.getLogger(FormProduto.class.getName()).log(Level.SEVERE, null, ex);
            }
            JOptionPane.showMessageDialog(this, "Modelo apagado com Sucesso");
        }
        support2.uninstall();
        combobox(cbmodelo);
    }//GEN-LAST:event_btdelimpActionPerformed


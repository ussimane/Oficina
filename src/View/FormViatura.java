    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (txtMat.getText().trim().equals("")) {
            lbValidacao.setVisible(true);
            lbValidacao.setText("Instroduza a Matricula");
            txtMat.requestFocusInWindow();
            return;
        }
        if (!(cbmodelo.getSelectedItem()instanceof Modelo)) {
            lbValidacao.setVisible(true);
            lbValidacao.setText("Seleccione um modelo");
            cbmodelo.requestFocusInWindow();
            return;
        }

        if (txtcor.getText().trim().equals("")) {
            lbValidacao.setVisible(true);
            lbValidacao.setText("Introduza a cor da viatura");
            txtcor.requestFocusInWindow();
            return;
        }
        if (jcClient.getSelectedItem() == null) {
            lbValidacao.setVisible(true);
            lbValidacao.setText("Seleccione um cliente");
            jcClient.requestFocusInWindow();
            return;
        }

        if (txtIdP.getText().trim().equals("")) {
            EntradaviaturaJpaController cc = new EntradaviaturaJpaController(emf);

            Entradaviatura e = new Entradaviatura();
            //  pro.setNuit(txtNuit.getText());
            e.setMatricula(txtMat.getText());
            e.setModelo((Modelo) cbmodelo.getSelectedItem());
            e.setCliente((Cliente) jcClient.getSelectedItem());
            e.setNomecliente(txtnome.getText());
            e.setDataentrada(dent.getDate());
            e.setDatasaida(dsaida.getDate());
            e.setCorviatura(txtcor.getText());
            try {
                cc.create(e);
            } catch (Exception ex) {
                Logger.getLogger(FormViatura.class.getName()).log(Level.SEVERE, null, ex);
            }
            limpar();
            JOptionPane.showMessageDialog(this, "Adicionado com Sucesso");
        } else {
            int respo = JOptionPane.showOptionDialog(null, "Pretende modificar os dados deste Viatura?", "ATENÇÃO!", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE,
                    null, new String[]{"SIM", "NÃO"}, null);
            if (respo == 1 || respo == -1) {
                return;
            }
            try {
                EntradaviaturaJpaController cc = new EntradaviaturaJpaController(emf);
                Entradaviatura e = cc.findEntradaviatura(Long.parseLong(txtIdP.getText()));
                //  pro.setNuit(txtNuit.getText());
                e.setMatricula(txtMat.getText());
                e.setModelo((Modelo) cbmodelo.getSelectedItem());
                e.setCorviatura(txtcor.getText());
                e.setCliente((Cliente) jcClient.getSelectedItem());
                e.setNomecliente(txtnome.getText());
                e.setDataentrada(dent.getDate());
                e.setDatasaida(dsaida.getDate());
                new EntradaviaturaJpaController(emf).edit(e);
                JOptionPane.showMessageDialog(this, "Editado com Sucesso");
                this.setVisible(false);
            } catch (NonexistentEntityException ex) {
                Logger.getLogger(FormProduto.class.getName()).log(Level.SEVERE, null, ex);
            } catch (Exception ex) {
                Logger.getLogger(FormProduto.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed
